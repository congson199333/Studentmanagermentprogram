    private void NutUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NutUpdateActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) BangSV.getModel();
        if(BangSV.getSelectedRow()==-1)
        {
            if(BangSV.getRowCount()==0){
                JOptionPane.showMessageDialog(this, "Không có trường nào trong bảng");
                }else{
                JOptionPane.showMessageDialog(this, "Bạn phải chọn 1 sinh viên");
            }
        }else{
            
            //update Oracle
            if(txtMSSV.getText().equals("")||txtTen.getText().equals("")||txtNgaysinh.getText().equals("")||
                txtDiachi.getText().equals("")||txtMalop.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập hết các trường!!!");
        }
        else {
                if(qlservice.xetMaLop(txtMalop.getText())==false){
                  JOptionPane.showMessageDialog(null, "Không tồn tại mã lớp tương ứng");
                  return;}
                
            
            if(txtMSSV.getText().length()!=8) {
            JOptionPane.showMessageDialog(this, "MSSV phải có 8 chữ số");
        }
        else{
            
            
            String MSSVX = model.getValueAt(BangSV.getSelectedRow(), 0).toString();
            
            if(qlservice.SuaDL(MSSVX, txtTen.getText(), txtNgaysinh.getText(), txtGioitinh.getSelectedItem().toString(),
                    txtDiachi.getText(), txtMalop.getText())==true)
            {
                JOptionPane.showMessageDialog(this, "Sửa dữ liệu thành công");   
                model.setValueAt(txtMSSV.getText(), BangSV.getSelectedRow(), 0);
                model.setValueAt(txtTen.getText(), BangSV.getSelectedRow(), 1);
                model.setValueAt(txtNgaysinh.getText(), BangSV.getSelectedRow(), 2);
                model.setValueAt(txtGioitinh.getSelectedItem().toString(), BangSV.getSelectedRow(), 3);
                model.setValueAt(txtDiachi.getText(), BangSV.getSelectedRow(), 4);
                model.setValueAt(txtMalop.getText(), BangSV.getSelectedRow(), 5);
            }else {
                JOptionPane.showMessageDialog(this, "Sửa dữ liệu không thành công");
            }
            }
            } 
        }
    }//GEN-LAST:event_NutUpdateActionPerformed

    
    
