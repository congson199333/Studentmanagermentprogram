    private void NutAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NutAddActionPerformed
        // TODO add your handling code here:
        if(txtMSSV.getText().equals("")||txtTen.getText().equals("")||txtNgaysinh.getText().equals("")||
                txtDiachi.getText().equals("")||txtMalop.getText().equals(""))
        {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập hết các trường!!!");
        }
        else {
            
              if(qlservice.xetMaLop(txtMalop.getText())==false){
                  JOptionPane.showMessageDialog(null, "Không tồn tại mã lớp tương ứng");
                  return;
              }
              
                
                
            
            if(txtMSSV.getText().length()!=8) {
            JOptionPane.showMessageDialog(this, "MSSV phải có 8 chữ số");
            
        }
            
            else {
                
            DefaultTableModel model = (DefaultTableModel) BangSV.getModel();
            
            if(qlservice.ThemDL(txtMSSV.getText(), txtTen.getText(), txtNgaysinh.getText(),
                    txtGioitinh.getSelectedItem().toString(), txtDiachi.getText(), txtMalop.getText())==true)
            {
                JOptionPane.showMessageDialog(null, "Thêm dữ liệu thành công");
                model.addRow(new Object[]{txtMSSV.getText(),txtTen.getText(),txtNgaysinh.getText(),
                                txtGioitinh.getSelectedItem(),txtDiachi.getText(),txtMalop.getText()});
            }else{
                JOptionPane.showMessageDialog(null, "Thêm dữ liệu không thành công");
            }
          

                }
        }
    }//GEN-LAST:event_NutAddActionPerformed

