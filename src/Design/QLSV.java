    private void NutDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NutDeleteActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) BangSV.getModel();
        if(BangSV.getSelectedRow()==-1)
        {
            if(BangSV.getRowCount()==0){
                JOptionPane.showMessageDialog(this, "Không có trường nào trong bảng");
                }else{
                JOptionPane.showMessageDialog(this, "Bạn phải chọn 1 sinh viên");
            }
        }else{
            //Delete Oracle
            int click = JOptionPane.showConfirmDialog(this, "Xóa sinh viên?","Question",JOptionPane.YES_NO_OPTION);
            if(click==JOptionPane.YES_OPTION)
            {
            String MSSVX = model.getValueAt(BangSV.getSelectedRow(), 0).toString();
            
            if(qlservice.XoaDL(MSSVX)==true)
            {
                JOptionPane.showMessageDialog(this, "Xóa dữ liệu thành công");
                    model.removeRow(BangSV.getSelectedRow());
                    this.txtMSSV.setText(null);
                    this.txtTen.setText(null);
                    this.txtNgaysinh.setText(null);
                    this.txtDiachi.setText(null);
                    this.txtMalop.setText(null);    
            }else{
                JOptionPane.showMessageDialog(this, "Xóa dữ liệu không thành công");
            }
        }else{
                return;
            }
    } 

    }//GEN-LAST:event_NutDeleteActionPerformed

    private void NutFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NutFindActionPerformed
        // TODO add your handling code here:
        String A = "";
        A = JOptionPane.showInputDialog("Nhập MSSV của sinh viên cần tìm: ");
        
        DefaultTableModel model = (DefaultTableModel) BangSV.getModel();
        
        int rowCount = model.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            model.removeRow(i);
        }
        model.setRowCount(0);
        
        if(qlservice.FindDL(A)==null){
            JOptionPane.showMessageDialog(null, "Không tìm thấy sinh viên!");
        }else{
            model.addRow(qlservice.FindDL(A));
        }
    }//GEN-LAST:event_NutFindActionPerformed

    private void NutListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NutListActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) BangSV.getModel();
        int rowCounts = model.getRowCount();
        for(int i = rowCounts - 1; i >= 0 ;i--){
            model.removeRow(i);
        }
        model.setRowCount(0);
        
        ArrayList<SinhVien> arrListSV=new ArrayList<SinhVien>();
        arrListSV=qlservice.ListDL();
        for(SinhVien a: arrListSV)
        {
            Vector<Object> vec =new Vector<Object>();
            vec.add(a.getMssv());
            vec.add(a.getTenSV());
            vec.add(a.getNgaysinh());
            vec.add(a.getGioiTinh());
            vec.add(a.getNoisinh());
            vec.add(a.getMaLop());
            model.addRow(vec);
            
        }
        
    }//GEN-LAST:event_NutListActionPerformed

